#include "fillit.h"

char	*ft_put_splitter(char *buff)
{
	int	i;

	i = 0;
	while(buff[i] != '\0')
	{
		if (buff[i] == '\n' && buff[i - 1] == '\n')
			buff[i] = '3';
		i++;
	}
	return (buff);
}
